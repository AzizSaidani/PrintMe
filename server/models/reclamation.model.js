const mongoose = require('mongoose');

const reclamationSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  phone: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

const Reclamation = mongoose.model('Reclamation', reclamationSchema);

module.exports = Reclamation;
